package com.retailsbs.logistikapp.user.domain;

public class Quantities {
	
	private Long alert_qty;
	private Long message_qty;
	private Long todo_qty;
	
	
	public Long getAlert_qty() {
		return alert_qty;
	}
	public void setAlert_qty(Long alert_qty) {
		this.alert_qty = alert_qty;
	}
	public Long getMessage_qty() {
		return message_qty;
	}
	public void setMessage_qty(Long message_qty) {
		this.message_qty = message_qty;
	}
	public Long getTodo_qty() {
		return todo_qty;
	}
	public void setTodo_qty(Long todo_qty) {
		this.todo_qty = todo_qty;
	}
	
	
	
	
}
