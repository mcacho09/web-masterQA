package com.retailsbs.logistikapp.logistic.dto;

public class MetricsCounterDTO {
	private int qty;
	private int total;
	private int visited;
	private int no_visited;
	
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getVisited() {
		return visited;
	}
	public void setVisited(int visited) {
		this.visited = visited;
	}
	public int getNo_visited() {
		return no_visited;
	}
	public void setNo_visited(int no_visited) {
		this.no_visited = no_visited;
	}
	
	
	
}
