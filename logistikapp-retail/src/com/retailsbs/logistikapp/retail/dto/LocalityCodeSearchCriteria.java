package com.retailsbs.logistikapp.retail.dto;

public class LocalityCodeSearchCriteria {
	
	private String active;
	private String code;
	private Long id_locality;

	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Long getId_locality() {
		return id_locality;
	}
	public void setId_locality(Long id_locality) {
		this.id_locality = id_locality;
	}

}
